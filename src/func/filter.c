#include "../ft_ls.h"

int	filter(unsigned char all, char *nam)
{
	if (all == 1 || nam[0] != '.')
		return (1);
	if (all == 2)
		if (ft_strcmp(nam, ".") != 0 && ft_strcmp(nam, "..") != 0)
			return (1);
	return (0);
}
