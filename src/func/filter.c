#include "../ft_ls.h"

int	filter(unsigned char all, char *nam)
{
	if (all == 1 || nam[0] != '.')
		return (1);
	if (all == 2)
		if (!ft_strequ(nam, ".") && !ft_strequ(nam, ".."))
			return (1);
	return (0);
}
